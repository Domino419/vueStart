let x = 5;
let y = "5" ; 
console.log( x == y) ;    // 값만 비교 true
console.log( x === y) ;   // 값과 타입 모두 비교 false
console.log( x != y) ;    // 값이 서로 다른지 비교 false
console.log( x !== y) ;    // 값이나 타입이 다른지 비교 true
console.log( x > 3) ;      //값이 특정 숫자보다 큰지 비교   true      
console.log( x <= 5 ) ;    // 값이 특정숫자보다 작거나 같은지 비교 true

